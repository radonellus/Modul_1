my_string = input("Введите текст: ") #команда для ввода значения
print(len(my_string)) #вывод значения в количестве символов
print(my_string .upper()) #вывод строки my_string в верхнем регистре
print(my_string .lower()) #вывод строки my_string в нижнем регистре
print(my_string .replace(' ','')) #вывод строки my_string удалив все пробелы
print(my_string[0]) #вывод первого символа строки my_string
print(my_string[-1]) #вывод последнего символа my_string
